#Definición de función con nombre HISTOGRAMA donde el usuario debe ingresar por pantalla los numeros.
#Definición de variable letra, que es equivalente a "x"
#El primer parámetro ingresado por el usuario, sera multiplicado por la letra, y así consecutivamente.
def histograma(uno,dos,tres):
    letra = "x"
    uletra = uno*letra
    dletra = dos*letra
    tletra = tres*letra
    print(uletra)
    print(dletra)
    print(tletra)
histograma(5,4,8)